"""
=============================================================================
STORAGE - Database for Encrypted Messages (PostgreSQL / SQLite)
=============================================================================

This module provides the Storage class for persisting:
1. Encrypted emails (local mailbox)
2. Pending messages (server broker queue)

DATABASE BACKENDS:
------------------
- **Production**: PostgreSQL (set DATABASE_URL env var)
  - Schema-based user isolation (each user gets their own schema)
  - Connection pooling with pool_pre_ping for resilience
  - Auto-creates schemas for per-user databases
- **Development**: SQLite (default fallback)
  - Per-file database (one SQLite file per user)
  - WAL mode for better concurrent access

DATABASE SCHEMA:
----------------

┌─────────────────────────────────────────────────────────────────────────┐
│  TABLE: emails (local mailbox)                                          │
├─────────────────────────────────────────────────────────────────────────┤
│  id                  INTEGER PRIMARY KEY  -- Unique email ID            │
│  sender              TEXT                 -- Sender email address       │
│  recipient           TEXT                 -- Recipient email address    │
│  subject             TEXT                 -- Email subject (encrypted?) │
│  ciphertext          BLOB                 -- Encrypted message body     │
│  mac                 BLOB (nullable)      -- MAC tag (for OTP)          │
│  signature           BLOB (nullable)      -- PQC digital signature      │
│  signature_algorithm TEXT (nullable)      -- e.g., "Dilithium2"         │
│  sent_at             DATETIME             -- When message was sent      │
│  view_once           BOOLEAN              -- Is this a view-once msg?   │
│  viewed              BOOLEAN              -- Has it been viewed?        │
│  otp_key             BLOB (nullable)      -- OTP key (view-once only)   │
│  mac_key             BLOB (nullable)      -- MAC key (view-once only)   │
│  key_exchange_mode   TEXT                 -- "pqc" or "bb84"            │
│  encryption_mode     TEXT                 -- "aes" or "view_once_otp"   │
│  folder              TEXT                 -- Inbox, Sent, Drafts, Trash │
│  delivery_status     TEXT                 -- sent, delivered, read      │
│  delivered_at        DATETIME (nullable)  -- When delivered             │
│  read_at             DATETIME (nullable)  -- When read                  │
│  server_message_id   TEXT (nullable)      -- UUID for broker tracking   │
└─────────────────────────────────────────────────────────────────────────┘

┌─────────────────────────────────────────────────────────────────────────┐
│  TABLE: pending_messages (server broker queue)                          │
├─────────────────────────────────────────────────────────────────────────┤
│  id                     TEXT PRIMARY KEY  -- UUID generated by sender   │
│  sender                 TEXT              -- Sender email               │
│  recipient              TEXT              -- Recipient email            │
│  subject                TEXT              -- Subject line               │
│  encrypted_content      BLOB              -- Encrypted message body     │
│  encryption_type        TEXT              -- "aes" or "otp"             │
│  key_material           BLOB              -- Session key or OTP keys    │
│  mac                    BLOB (nullable)   -- MAC tag                    │
│  signature              BLOB (nullable)   -- PQC signature              │
│  signature_algorithm    TEXT (nullable)   -- Signature algorithm        │
│  key_exchange_algorithm TEXT              -- "pqc" or "bb84"            │
│  view_once              BOOLEAN           -- View-once flag             │
│  status                 TEXT              -- pending, acknowledged      │
│  created_at             DATETIME          -- When uploaded              │
│  downloaded_at          DATETIME (null)   -- When recipient downloaded  │
│  acknowledged_at        DATETIME (null)   -- When receipt confirmed     │
└─────────────────────────────────────────────────────────────────────────┘

SECURITY NOTES:
---------------
1. CIPHERTEXT ONLY: The `ciphertext` column stores encrypted data.
   Plaintext is NEVER written to the database.

2. VIEW-ONCE KEYS: For OTP-encrypted messages, the `otp_key` and `mac_key`
   are temporarily stored until first viewing. After viewing:
   - The `viewed` flag is set to True
   - Keys should be zeroized in memory
   - The message can never be decrypted again

3. KEY MATERIAL: The `key_material` column in `pending_messages` stores
   the session key (for AES) or OTP keys (for OTP). This is encrypted
   during transit using PQC key exchange.

4. SIGNATURES: PQC signatures are stored to enable verification even
   after message delivery, providing non-repudiation.

USAGE:
------
    from qmail.storage.db import Storage

    # Production (PostgreSQL):
    storage = Storage(database_url="postgresql://user:pass@localhost/qmail")

    # Per-user isolation with PostgreSQL schemas:
    storage = Storage(
        database_url="postgresql://user:pass@localhost/qmail",
        schema="user_alice_at_qmail_com",
    )

    # Development (SQLite - backward compatible):
    from pathlib import Path
    storage = Storage(db_path=Path("mailbox.db"))

    # Save an email
    email_id = storage.save_email(email_envelope)

    # Get an email
    email = storage.get_email(email_id)

    # List all emails
    for email in storage.list_emails():

    # Server broker operations
    storage.save_pending_message(...)
    messages = storage.list_pending_messages("recipient@example.com")
    storage.mark_message_downloaded("message-uuid")
    storage.delete_pending_message("message-uuid")
"""

