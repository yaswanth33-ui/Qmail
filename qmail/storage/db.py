            # If no existing key, insert new one
            if result.rowcount == 0:
                stmt = signing_keys_table.insert().values(
                    email=email,
                    public_key=public_key,
                    private_key=encrypted_pk,
                    algorithm=algorithm,
                    created_at=now,
                    updated_at=None,
                )
                conn.execute(stmt)

